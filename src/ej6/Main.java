package ej6;

public class Main {

	public static void main(String[] args) {
		
		//
		int [] t= {10,1,5,8,9,2};
		
	}
	
	int[] suma(int t[], int numElementos) {
		
		//
		int[] sumaCifras;
		
		
		for(int i=0;i<t.length;i++) {
			//por cada numero de elemento a sumar
			for(int j=0;j<numElementos;i++) {
				//le aumentamos el valor a sumaCifras
				sumaCifras[i]+=t[i+j];
			}
			
			
		}
		
		
		
		
		return t;
	}
	
}
